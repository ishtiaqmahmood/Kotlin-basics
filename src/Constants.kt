fun main(args:Array<String>) {
    // Constants
    // Value of Constants can't be changed

    val number: Int = 10 // Int: Stores Integers
    val pi: Double = 3.14159 // Double: Stores Doubles
    val k: Float = 5.19F  // Float: Stores Floats
    val name: String = "Gatling" // String: Stores multiple Characters

    println("Int: $number Double: $pi Float: $k String: $name")
    println("Int: " + number + " Double: " + pi + " Float: " + k + " String: " + name)
}