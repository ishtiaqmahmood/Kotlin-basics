fun main(args:Array<String>) {
    // Variable
    // Value of Variables can be changed

    var number: Int = 10 // Int: Stores Integers
    var pi: Double = 3.14159 // Double: Stores Doubles
    var k: Float = 5.19F  // Float: Stores Floats
    var name: String = "Gatling" // String: Stores multiple Characters

    // Changing var Values
    number = 11
    pi = 3.1416
    k = 4.67F
    name = "John"

    println("Int: $number Double: $pi Float: $k String: $name")
    println("Int: " + number + " Double: " + pi + " Float: " + k + " String: " + name)
}